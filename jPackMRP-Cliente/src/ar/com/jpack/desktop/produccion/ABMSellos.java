    public ABMSellos() {
        super(new SellosT());
        initComponents();
        HashMap parametros = new HashMap();
        parametros.put("pJoinEstados", true);
        parametros.put("pJoinArticulos", true);
        List<SellosT> nuevo = DesktopApp.getApplication().getSellosT(parametros);
        setListDto((ArrayList<SellosT>) nuevo);

        tableModel = new SellosTableModel(columnNames, this.getListDto());
        tableModel.addTableModelListener(new CustomTableModelListener());
        tblSellos.setModel(tableModel);

        sorter = new TableRowSorter<TableModel>(tableModel) {

            @Override
            public void toggleSortOrder(int column) {
                RowFilter<? super TableModel, ? super Integer> f = getRowFilter();
                setRowFilter(null);
                super.toggleSortOrder(column);
                setRowFilter(f);
            }
        };
        tblSellos.setRowSorter(sorter);

        setModificado(false);
        setNuevo(false);
        txtDescripcion.setEnabled(false);
        txtTamano.setEnabled(false);
        cmbArticulo.setEnabled(false);
        txtEstado.setEnabled(false);

        if (getPadre() == null) {
            btnSeleccionar.setEnabled(false);
        }

        parametros = new HashMap();

        tblSellos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

